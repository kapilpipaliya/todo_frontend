<script>
  import { navigateTo, routeIsActive } from '../router.js'
  export let to = '/'
  export let title = ''
  export let styles = ''

  function navigate(event) {
    event.preventDefault()
    event.stopPropagation()
    navigateTo(to)
  }
</script>

<a href={to} {title} on:click={navigate} class={styles} class:active={routeIsActive(to)}>
  <slot />
</a>
